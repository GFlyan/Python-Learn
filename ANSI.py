# O código ANSI (scape sequence) é utilizado para atribuir cores à uma linha de código.

# Sua estrutura consiste em: \033['style';'text';'back'm

# Style - Corresponde ao estilo do texto, que pode ser:
# 0 - None (Nenhum) / 1 - Bold (Negrito) / 4 - Underline / 7 - Negative (Cores negativas/invertidas)

# Text - Corresponde a cor do texto, que pode ser:
# 30 - Branco / 31 - Vermelho / 32 - Verde / 33 - Amarelo / 34 - Azul / 35 - Roxo / 36 - Ciano / 37 -Cinza

# Back - Corresponde a cor do fundo do texto, que pode ser:
# 40 - Branco / 41 - Vermelho / 42 - Verde / 43 - Amarelo / 44 - Azul / 45 - Roxo / 46 - Ciano / 47 - Cinza

